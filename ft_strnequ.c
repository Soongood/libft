#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2)
	{
		while (*s1 == *s2 && *s1 != '\0' && n)
		{
			s1++;
			s2++;
			n--;
		}
		if ((!*s2 && !*s1) || !n)
			return (1);
	}
	return (0);
}
