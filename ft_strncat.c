#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *p;

	p = s1;
	while (*s1)
		s1++;
	while (n--)
	{
		if ((*s1 = '\0') == *s2)
			return (p);
		*s1++ = *s2++;
	}
	*s1 = '\0';
	return (p);
}
