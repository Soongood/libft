#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char *p;

	p = (unsigned char *)b;
	while (len--)
		*(unsigned char *)b++ = (unsigned char)c;
	return ((unsigned char *)p);
}
